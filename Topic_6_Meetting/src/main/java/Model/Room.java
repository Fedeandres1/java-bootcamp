package Model;

/**
 *
 * @author Frederic
 */
public class Room {
    private int number;
    private int floor;
    private String zone;

    public Room(int number, int floor, String zone) {
        this.number = number;
        this.floor = floor;
        this.zone = zone;
    }

    public Room() {
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getFloor() {
        return floor;
    }

    public void setFloor(int floor) {
        this.floor = floor;
    }

    public String getZone() {
        return zone;
    }

    public void setZone(String zone) {
        this.zone = zone;
    }
    
}
